package cn.zilion.pojo;

public class CommodityPropertyMap {
    String commodityPropertyName;
    String commodityPropertyValue;

    public String getCommodityPropertyName() {
        return commodityPropertyName;
    }

    public void setCommodityPropertyName(String commodityPropertyName) {
        this.commodityPropertyName = commodityPropertyName;
    }

    public String getCommodityPropertyValue() {
        return commodityPropertyValue;
    }

    public void setCommodityPropertyValue(String commodityPropertyValue) {
        this.commodityPropertyValue = commodityPropertyValue;
    }
}
