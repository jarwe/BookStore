package cn.zilion.pojo;

public class CommodityPropertyName {
    Integer commodityPropertyNameId;
    Integer commodityTypeId;
    String commodityPropertyName;

    public Integer getCommodityPropertyNameId() {
        return commodityPropertyNameId;
    }

    public void setCommodityPropertyNameId(Integer commodityPropertyNameId) {
        this.commodityPropertyNameId = commodityPropertyNameId;
    }

    public Integer getCommodityTypeId() {
        return commodityTypeId;
    }

    public void setCommodityTypeId(Integer commodityTypeId) {
        this.commodityTypeId = commodityTypeId;
    }

    public String getCommodityPropertyName() {
        return commodityPropertyName;
    }

    public void setCommodityPropertyName(String commodityPropertyName) {
        this.commodityPropertyName = commodityPropertyName;
    }
}
