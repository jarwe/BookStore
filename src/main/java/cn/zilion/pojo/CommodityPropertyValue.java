package cn.zilion.pojo;

public class CommodityPropertyValue {
    Integer commodityPropertyValueId;
    Integer commodityPropertyNameId;
    Integer commodityId;
    String commodityPropertyValue;

    public Integer getCommodityPropertyValueId() {
        return commodityPropertyValueId;
    }

    public void setCommodityPropertyValueId(Integer commodityPropertyValueId) {
        this.commodityPropertyValueId = commodityPropertyValueId;
    }

    public Integer getCommodityPropertyNameId() {
        return commodityPropertyNameId;
    }

    public void setCommodityPropertyNameId(Integer commodityPropertyNameId) {
        this.commodityPropertyNameId = commodityPropertyNameId;
    }

    public Integer getCommodityId() {
        return commodityId;
    }

    public void setCommodityId(Integer commodityId) {
        this.commodityId = commodityId;
    }

    public String getCommodityPropertyValue() {
        return commodityPropertyValue;
    }

    public void setCommodityPropertyValue(String commodityPropertyValue) {
        this.commodityPropertyValue = commodityPropertyValue;
    }
}
